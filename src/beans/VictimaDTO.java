package beans;

public class VictimaDTO {
	private String codvic;
	private String nomvic;
	private String apevic;
	private String dni;
	private String sexo;

	public String getCodvic() {
		return codvic;
	}

	public void setCodvic(String codvic) {
		this.codvic = codvic;
	}

	public String getNomvic() {
		return nomvic;
	}

	public void setNomvic(String nomvic) {
		this.nomvic = nomvic;
	}

	public String getApevic() {
		return apevic;
	}

	public void setApevic(String apevic) {
		this.apevic = apevic;
	}

	public String getDni() {
		return dni;
	}

	public void setDni(String dni) {
		this.dni = dni;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}

}
