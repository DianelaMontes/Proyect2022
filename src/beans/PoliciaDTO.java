package beans;

public class PoliciaDTO {

	
	private String codpol ;
	private String nompol ;
	private String apepol;
	private int cargo;
	private String dni;
	private String codcom ;
	private String sexo;
	public String getCodpol() {
		return codpol;
	}
	public void setCodpol(String codpol) {
		this.codpol = codpol;
	}
	public String getNompol() {
		return nompol;
	}
	public void setNompol(String nompol) {
		this.nompol = nompol;
	}
	public String getApepol() {
		return apepol;
	}
	public void setApepol(String apepol) {
		this.apepol = apepol;
	}
	public int getCargo() {
		return cargo;
	}
	public void setCargo(int cargo) {
		this.cargo = cargo;
	}
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getCodcom() {
		return codcom;
	}
	public void setCodcom(String codcom) {
		this.codcom = codcom;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	
	
	

}
